package Fundamentos;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
		int a = 7;
		int b = 3;
		
		b += a;
		System.out.println(b);
		b *= 4;
		System.out.println(b);
		b /= 2;
		System.out.println(b);
		b -= 2;
		System.out.println(b);
		b %= 2;
		System.out.println(b);
		
	}
}
