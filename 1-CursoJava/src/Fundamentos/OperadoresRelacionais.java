package Fundamentos;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		// == != >= > <= <
		int x = 97;
		int y = 'a';
		System.out.println(x == y);
		System.out.println(x != y);
		System.out.println(x >= y);
		System.out.println(x > y);
		System.out.println(x <= y);
		System.out.println(x < y);
		
		
		
	}
}
